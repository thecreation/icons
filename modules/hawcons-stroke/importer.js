const generateCss = require('../../scripts/utils/generateCss');
const generateJson = require('../../scripts/utils/generateJson');
const prepareIcons = require('../../scripts/utils/prepareIcons');
const extraFromJson = require('../../scripts/utils/extraFromJson');
const detectLicense = require('../../scripts/utils/detectLicense');
const getIconsFromCss = require('../../scripts/utils/getIconsFromCss');
const getSvgs = require('../../scripts/utils/getSvgs');
const copySvgs = require('../../scripts/utils/copySvgs');
const optimizeSvgs = require('../../scripts/utils/optimizeSvgs');
const getFonts = require('../../scripts/utils/getFonts');
const copyFonts = require('../../scripts/utils/copyFonts');
const copyLicense = require('../../scripts/utils/copyLicense');
const jsonfile = require('../../scripts/utils/jsonfile');
const clean = require('../../scripts/utils/clean');
const config = require('../../config');
const path = require('path');

let options = {
  source: path.join(config.sets.customs, 'Hawcons', 'Font', 'Stroke'),
  name: 'hawcons-stroke',
  class: 'hst',
  prefix: 'hst-',
  className: 'HawconsStroke',
  title: 'Hawcons Stroke',
  homepage: 'http://hawcons.com/preview/',
  description: 'An Icon Font Generated By Yannick Lung',
  license: 'MIT',
  author: 'Yannick Lung',
  version: '1.0.0',
  classifiable: false,
};

let paths = {
  css: path.join(options.source, 'style.css'),
  fonts: path.join(options.source, 'fonts'),
  svgs: path.join(config.sets.customs, 'Hawcons', 'SVG'),
  license: path.join(config.sets.customs, 'Hawcons', 'Read Me.md'),
  dest: __dirname,
  svgsDest: path.join(__dirname, 'icons')
};

options.fonts = getFonts(paths.fonts);
options.svgs = getSvgs(paths.svgs, '**/Stroke/*.svg');
// options.license = detectLicense(paths.license);

module.exports = function(callback) {
  clean(paths.dest);
  options.icons = getIconsFromCss(paths.css, 'icon-icon-', "\\.{prefix}\\d+-([\\w\\d-]+):before");
  options = prepareIcons(options);
  generateCss(paths.dest, options.name, options);
  generateJson(paths.dest, options);
  copyFonts(paths.dest, paths.fonts, options);
  options.svgs = copySvgs(paths.svgsDest, paths.svgs, options.svgs, null, function(name) {
    return path.basename(name).replace(/^icon-(\d{1,3})-{1,2}/i, '');
  });
  optimizeSvgs(paths.svgsDest, options.svgs);
  copyLicense(paths.dest, paths.license);
  jsonfile(paths.dest, options);
  callback();
};
